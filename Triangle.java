import java.awt.*;

class Triangle {
	double x1; //storing the points as doubles
	double y1;
	
	double x2;
	double y2;
	
	double x3;
	double y3;
	
    Triangle(double x1, double y1, double x2, double y2, double x3, double y3) { //Constructor
    	this.x1 = x1;
    	this.y1 = y1;
    	
    	this.x2 = x2;
    	this.y2 = y2;
    	
    	this.x3 = x3;
    	this.y3 = y3;
    }
}

